package com.dto.pojo.stata;

public class StatDTO {
    private String dm;
    private String mc;
    private String statSum;

    public String getDm() {
        return dm;
    }

    public void setDm(String dm) {
        this.dm = dm;
    }

    public String getMc() {
        return mc;
    }

    public void setMc(String mc) {
        this.mc = mc;
    }

    public String getStatSum() {
        return statSum;
    }

    public void setStatSum(String statSum) {
        this.statSum = statSum;
    }
}
